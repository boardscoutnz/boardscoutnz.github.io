async function ingestJson(blob) {
  dbgGroup('data', 'ingestJson');
  let raw;
  let exportedAt = null;
  let exportType = null;
  if (Array.isArray(blob)) {
    raw = blob;
    dbg('data', `input shape: bare array (legacy), length=${raw.length.toLocaleString()}`);
  } else if (blob && Array.isArray(blob.listings)) {
    raw = blob.listings;
    exportedAt = blob.exportedAt || null;
    exportType = blob.exportType || 'full';
    dbg('data', `input shape: wrapped object`, {
      listings: raw.length,
      sellers: Array.isArray(blob.sellers) ? blob.sellers.length : 0,
      exportedAt,
      exportType,
    });
  } else {
    dbgError('data', 'unrecognized JSON shape — expected {listings: [...]} object', blob);
    console.error('[bsnz] unrecognized JSON shape — expected {listings: [...]} object');
    showEmptyState('unrecognized JSON shape');
    dbgGroupEnd('data');
    return;
  }

  if (exportType === 'delta') {
    dbgError('data', 'delta-only export detected; cannot render without base corpus');
    console.error('[bsnz] delta-only export detected; cannot render without base corpus');
    showEmptyState('committed file is a delta-only export, not a full snapshot');
    dbgGroupEnd('data');
    return;
  }

  dbgTime('normalise listings');
  listings = raw.map((r) => normaliseImportedListing(r)).filter(Boolean);
  dbgTimeEnd('normalise listings');
  const dropped = raw.length - listings.length;
  dbg('data', `normalised: ${listings.length.toLocaleString()} listings (${dropped.toLocaleString()} dropped during normalisation)`);

  if (!listings.length) {
    dbgError('data', 'committed file contained no usable listings after normalisation');
    showEmptyState('the committed file contained no listings');
    dbgGroupEnd('data');
    return;
  }

  // Distribution of conditions + accessory flag, useful when the
  // sidebar counts look wrong — you can compare the snapshot here to
  // the counts in updateStatsBar.
  const distribution = { condition: {}, expansion: 0, newListing: 0 };
  for (const l of listings) {
    const c = l.condition || '(null)';
    distribution.condition[c] = (distribution.condition[c] || 0) + 1;
    if (l.isExpansion)   distribution.expansion++;
    if (l.isNewListing)  distribution.newListing++;
  }
  dbg('data', 'distribution after normalisation:', distribution);

  // v1.6.9: enrichment is now async (chunked, yields to event loop).
  // Show the grid FIRST so the user sees data immediately, then run
  // enrichment in the background and push the BGG-decorated rows into
  // the grid via replaceData() once it finishes. Without this ordering,
  // a 20-second match run delays the grid appearing for 20 seconds —
  // which is the exact UX bug we're fixing.
  updateDataMetaPill(exportedAt);
  dbg('data', 'ingestion complete — calling showGrid (BGG enrichment will run in background)');
  dbgGroupEnd('data');
  showGrid();

  if (bgg.loaded) {
    dbg('data', 'BGG cache already loaded — running enrichment in background');
    enrichListingsWithBgg(listings).then(() => {
      if (table) {
        dbg('data', 'background enrichment complete — pushing enriched rows into grid');
        table.replaceData(listings);
        table.redraw(true);   // v1.6.13: prevent virtual-DOM scroll desync (see applyFilters)
        dbg('data', 'replaceData + redraw(true) complete');
      }
      updateStatsBar();
    }).catch((e) => {
      dbgError('data', 'background enrichment threw:', e && e.message);
    });
  } else {
    dbg('data', 'BGG cache not yet loaded — enrichment will run from loadBggCache when it finishes');
  }
}

function normaliseImportedListing(r) {
  if (!r) return null;
  const listingId = num(r.listingId);
  if (!listingId) return null;

  let price = num(r.priceNumeric);
  if (price == null) price = num(r.price);
  if (price == null) price = num(r.buyNowPrice) ?? num(r.startPrice);

  const title = String(r.title || '').trim();

  // v1.6.17: district + suburb dropped from the row schema; they
  // were null on 100 % of 7,223 production listings (verified
  // against a snapshot generated by userscript v0.7.8). The grid
  // column still binds to `regionDisplay` so the field is kept,
  // but it now just mirrors `region` — no more "/"-joined
  // multi-segment string. isClassified was dropped at the same
  // time and the "Classified only" sidebar filter was retired.
  const regionDisplay = r.region || '';

  // v1.6.18: endDate + closed dropped. The userscript stopped
  // exporting endDate in v0.7.10 (TM never populated it); the
  // derived `closed` field was always false and the "Hide closed"
  // sidebar checkbox it powered was wired to nothing anyway.
  return {
    listingId,
    title,
    url: r.url || `https://www.trademe.co.nz/a/marketplace/listing/${listingId}`,
    subcat: r.subcat || null,
    condition: r.condition || null,
    price: price,
    priceLabel: r.priceLabel || null,
    priceDisplay: r.priceDisplay || null,
    hasBuyNow: r.hasBuyNow === true || r.hasBuyNow === 'true',
    region: r.region || null,
    regionDisplay: regionDisplay,
    // Userscript v0.7.11+ tags expansion-only listings with
    // isExpansion=true (using the "and / + / inc / including / comes"
    // exemption rule — see userscript). Older snapshots have no such
    // field; coerce to false so the default 'board-games' view shows
    // them rather than orphaning them. The legacy isAccessory field
    // was retired when the Accessories mode was removed; its trigger
    // words were folded into the userscript's PURGE_TITLE_RX so those
    // listings never reach the export now.
    isExpansion: !!r.isExpansion,
    isNewListing: !!r.isNewListing,   // v1.6.13: from userscript v0.7.6+
    bgg_id: null, bgg_name: null, bgg_rank: null, bgg_average: null,
    bgg_weight: null, bgg_min_players: null, bgg_max_players: null,
    bgg_playing_time: null, bgg_year: null,
    bgg_match_confidence: 'none', bgg_match_score: null,
  };
}

function num(v) {
  if (v == null || v === '') return null;
  const n = Number(v);
  return Number.isFinite(n) ? n : null;
}

